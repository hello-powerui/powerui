export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ icon: string }> }
) {
  const searchParams = request.nextUrl.searchParams;
  const color = searchParams.get('color');
  const size = searchParams.get('size') || '24';
  
  const { icon } = await params;
  const iconPath = iconPaths[icon];
  
  if (!iconPath) {
    // Try to fetch from Lucide's GitHub repo as fallback
    try {
      const response = await fetch(`https://raw.githubusercontent.com/lucide-icons/lucide/main/icons/${icon}.svg`);
      if (response.ok) {
        let svgContent = await response.text();
        
        // If color is provided, replace currentColor with it
        if (color) {
          svgContent = svgContent.replace(/stroke="currentColor"/g, `stroke="${color}"`);
        }
        
        svgContent = svgContent.replace(/width="24"/g, `width="${size}"`);
        svgContent = svgContent.replace(/height="24"/g, `height="${size}"`);
        
        return new NextResponse(svgContent, {
          headers: {
            'Content-Type': 'image/svg+xml',
            'Cache-Control': 'public, max-age=31536000, immutable',
            'Access-Control-Allow-Origin': '*',
          },
        });
      }
    } catch (error) {
      console.error('Failed to fetch from GitHub:', error);
    }
    
    return new NextResponse('Icon not found', { status: 404 });
  }
  
  // Generate SVG - use currentColor by default, or specific color if provided
  const strokeColor = color || 'currentColor';
  const svg = `<svg xmlns="http://www.w3.org/2000/svg" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="${strokeColor}" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
  <path d="${iconPath}" />
</svg>`;
  
  return new NextResponse(svg, {
    headers: {
      'Content-Type': 'image/svg+xml',
      'Cache-Control': 'public, max-age=31536000, immutable',
      'Access-Control-Allow-Origin': '*',
    },
  });
}